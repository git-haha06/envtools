export const useI18nStore = defineStore('i18n', {
  state: () => ({
    locale: 'zh-CN',
  }),
  actions: {
    setLocale(locale) {
      this.locale = locale
      localStorage.setItem('envtools-locale', locale)
    },
    initLocale() {
      const saved = localStorage.getItem('envtools-locale')
      if (saved && (saved === 'zh-CN' || saved === 'en')) {
        this.locale = saved
      }
    },
    t(key) {
      return translations[this.locale]?.[key] ?? translations['zh-CN'][key] ?? key
    },
  },
})
